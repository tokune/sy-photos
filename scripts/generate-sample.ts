// 生成 Synology 元数据
function generateMetadata(
  dateStr: string,
  people: string[],
  location: { name: string; lat: number; lng: number } | null
) {
  // 生成带有时间的日期
  const hour = 8 + Math.floor(Math.random() * 12);
  const minute = Math.floor(Math.random() * 60);
  const takenAt = new Date(`${dateStr}T${String(hour).padStart(2, "0")}:${String(minute).padStart(2, "0")}:00`);

  return {
    version: 1,
    takenAt: takenAt.toISOString(),
    face: people.map((name, i) => ({
      id: `face-${i}`,
      name,
      x: Math.random() * 0.5,
      y: Math.random() * 0.5,
      w: 0.2,
      h: 0.2,
    })),
    geocoding: location
      ? {
          country: "中国",
          city: location.name,
          address: `${location.name}市中心`,
          latitude: location.lat,
          longitude: location.lng,
        }
      : undefined,
  };
}

async function generateSampleData() {
  console.log("Generating sample photo data...");
  console.log(`Today is ${todayMonth}-${todayDay}, generating "years ago" test data...\n`);

  let photoCount = 0;

  for (const album of sampleAlbums) {
    const albumPath = join(PHOTOS_DIR, album.name);
    await mkdir(albumPath, { recursive: true });

    // 为每个日期生成3-5张照片
    for (const dateStr of album.dates) {
      const photosPerDay = 3 + Math.floor(Math.random() * 3);

      for (let i = 0; i < photosPerDay; i++) {
        const filename = `IMG_${dateStr.replace(/-/g, "")}_${String(i + 1).padStart(3, "0")}.jpg`;
        const photoPath = join(albumPath, filename);

        // 生成占位图片
        const imageData = generatePlaceholderImage();
        await Bun.write(photoPath, imageData);

        // 生成 @eaDir 元数据
        const eaDirPath = join(albumPath, "@eaDir", filename);
        await mkdir(eaDirPath, { recursive: true });

        // 随机选择1-3个人物
        const numPeople = 1 + Math.floor(Math.random() * 3);
        const shuffledPeople = [...samplePeople].sort(() => Math.random() - 0.5);
        const selectedPeople = shuffledPeople.slice(0, numPeople);

        // 随机选择地点 (70% 概率有地点)
        const location = Math.random() > 0.3
          ? sampleLocations[Math.floor(Math.random() * sampleLocations.length)] ?? null
          : null;

        // 写入元数据（包含日期）
        const metadata = generateMetadata(dateStr, selectedPeople, location);
        await Bun.write(
          join(eaDirPath, "SYNOPHOTO_METADATA.json"),
          JSON.stringify(metadata, null, 2)
        );

        // 复制图片作为缩略图
        await Bun.write(join(eaDirPath, "SYNOPHOTO_THUMB_XL.jpg"), imageData);

        photoCount++;
      }
    }
  }

  console.log(`Generated ${photoCount} sample photos in ${sampleAlbums.length} albums`);
  console.log("\nSample data structure:");
  console.log("- photos/");
  for (const album of sampleAlbums) {
    console.log(`  - ${album.name}/`);
  }
  console.log("\nFeatures to test:");
  console.log(`  - "X年前的今天": Photos from 2021, 2022, 2023 on ${todayMonth}-${todayDay}`);
  console.log("  - 人物同框: Multiple people in same photos");
  console.log("  - 地点故事: Photos grouped by location (北京, 上海, 杭州, 成都)");
  console.log("\nRun 'bun run dev' to start the server");
}

generateSampleData().catch(console.error);
